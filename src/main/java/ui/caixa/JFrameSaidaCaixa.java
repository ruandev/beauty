    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "<html>Deseja realmente realizar essa <b>Saída</b> no caixa?</html>","Galera de casa aí, comé que é, meu",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(dialogResult == JOptionPane.YES_OPTION){
            try {
                this.buildMovimento();
                repository.inserirMovimentoCaixa(movimento);
                this.limparCampos();
                JOptionPane.showMessageDialog(this, "SaÃ¯Â¿Â½da de caixa realizada com sucesso!", "Quem sabe faz ao vivo!", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
                JOptionPane.showMessageDialog(this, "Ocorreu um erro ao tentar realizar a Saída de Caixa", "Errrrôôôôôuuuu!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void campoValorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoValorFocusLost
        try {
            double valor = Mascaras.converteDouble(campoValor.getText());
            campoValor.setText(Mascaras.monetario.format(valor));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Valor Inválido!", "Errrrôôôôôuuuu!", JOptionPane.ERROR_MESSAGE);
            campoValor.setText("");
            campoValor.requestFocus();
        }
    }//GEN-LAST:event_campoValorFocusLost

